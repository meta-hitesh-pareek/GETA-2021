package GETA2021.SCF.SCFAssignment9;

/**
 * Create a point on the screen
 * 
 * @since 03-03-2021
 * @author Hitesh Pareek
 *
 */
public class Point {
	double xCoordinate, yCoordinate;

	/**
	 * Constructor to create then point with given coordinate
	 * 
	 * @param xOrigin
	 * @param yOrigin
	 */
	Point(double xOrigin, double yOrigin) {
		this.xCoordinate = xOrigin;
		this.yCoordinate = yOrigin;
	}
}
